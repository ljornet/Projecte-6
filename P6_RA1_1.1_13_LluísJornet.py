# Demanem la temperatura en graus Celsius
tempC = int(input("Dona els graus en Celsius: "))

# Calculem la temperatura en Fahrenheit
tempF = (tempC * 9 / 5) + 32

# Mostrem el resultat
print(tempC, "Celsius són", tempF, "Fahrenheit")
