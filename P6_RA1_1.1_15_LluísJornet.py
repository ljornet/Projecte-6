# Demanem la distància en quilòmetres i el temps en hores
Km = int(input("Distancia(Kilometres): "))
Hrs = int(input("Temps(Hores): "))

# Calculem la mitjana de la velocitat
Mitjana = (Km / Hrs)

# Mostrem el resultat
print("La mitjana de la velocitat es de: ", Mitjana)
