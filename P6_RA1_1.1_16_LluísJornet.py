# Demanem els tres números
num1 = int(input("Introdueix el primer número: "))
num2 = int(input("Introdueix el segon número: "))
num3 = int(input("Introdueix el tercer número: "))

# Calculem la mitjana
mitjana = (num1 + num2 + num3) / 3

# Mostrem el resultat
print("La mitjana dels tres números és: ", mitjana)