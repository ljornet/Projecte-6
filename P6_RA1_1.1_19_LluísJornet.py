# Demanem la llargada i l'amplada de l'habitació
llargada = int(input("Quina és la llargada de l'habitació en metres? "))
amplada = int(input("Quina és l'amplada de l'habitació en metres? "))

# Calculem la superfície
superficie = llargada * amplada

# Mostrem el resultat
print("La superfície de l'habitació és: ", superficie, "m²")
